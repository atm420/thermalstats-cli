pub fn detect_locale(override_lang: Option<&str>) -> String {
    if let Some(lang) = override_lang {
        return normalize_locale(lang);
    }
    sys_locale::get_locale()
        .map(|l| normalize_locale(&l))
        .unwrap_or_else(|| "en".to_string())
}

fn normalize_locale(locale: &str) -> String {
    let lower = locale.to_lowercase();
    // Extract the language code (first 2 chars before any separator)
    let code = lower
        .split(|c: char| c == '-' || c == '_')
        .next()
        .unwrap_or("en");
    match code {
        "fr" | "es" | "de" | "pt" | "tr" | "ru" | "ko" | "ar" => code.to_string(),
        _ => "en".to_string(),
    }
}

